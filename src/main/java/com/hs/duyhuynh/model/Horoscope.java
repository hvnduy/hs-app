package com.hs.duyhuynh.model;

public class Horoscope {
	private int horoscopeId;
	private String horoscopeName;
	private String horoscopeDesc;
	private String horoscopeImg;
	public int getHoroscopeId() {
		return horoscopeId;
	}
	public void setHoroscopeId(int horoscopeId) {
		this.horoscopeId = horoscopeId;
	}
	public String getHoroscopeName() {
		return horoscopeName;
	}
	public void setHoroscopeName(String horoscopeName) {
		this.horoscopeName = horoscopeName;
	}
	public String getHoroscopeDesc() {
		return horoscopeDesc;
	}
	public void setHoroscopeDesc(String horoscopeDesc) {
		this.horoscopeDesc = horoscopeDesc;
	}
	public String getHoroscopeImg() {
		return horoscopeImg;
	}
	public void setHoroscopeImg(String horoscopeImg) {
		this.horoscopeImg = horoscopeImg;
	}

}
